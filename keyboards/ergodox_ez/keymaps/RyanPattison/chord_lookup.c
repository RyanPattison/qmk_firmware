char const * /** Nullable */chord_lookup(int32_t chord) {
  int lower = 0, upper = STENO_WORD_COUNT - 1;
  while (lower <= upper) {
      int middle = lower + (upper - lower) / 2;
      int32_t diff = chords[middle] - chord;
      if (diff < 0) {
         lower = middle + 1;
      } else if (diff > 0) {
         upper = middle - 1;
      } else {
         return steno_words[middle];
      }
  }
  return NULL;
}

